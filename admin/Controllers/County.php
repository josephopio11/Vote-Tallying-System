    /**
     * create or update
     * @return json
     */
    public function save($form_slug, $countyid = '')
    {
        $data       = $this->request->getPost();
        $validation = \Config\Services::validation();

        //---------------------------------------------------------------------//
        //For form_slug=county pageSlug=county
        if($form_slug === 'county')
        {

            // county form Validations and security
            $validation->setRules([
                'zoneid'        => ['label' => lang('county.zoneid'), 'rules'=>"required|integer|max_length[11]"],
                'name'          => ['label' => lang('county.name'), 'rules'=>"required|string|max_length[25]"],
            ]);
            if ($validation->run($data) === false)
            {
                return $this->fail($validation->getErrors(), 400, lang('home.error_validation_form'));
            }
            //----------------------------------------------------------------//
            // clear undefined post fields for security..
            //----------------------------------------------------------------//
            $filterFormFields = ['zoneid', 'name', 'countyid'];
            $data             = array_intersect_key($data, array_flip($filterFormFields));
            //----------------------------------------------------------------//

            //----------------------------------------------------------------//
            /**
            * Check zoneid for security
            */        
            //----------------------------------------------------------------//
            if (empty($this->request->getPost('new_zoneid'))  && empty(getC4_zone($data['zoneid'])))
            {
                log_message('error', 'SECURITY: Undefined zoneid value posted');
                return $this->fail(['zoneid'=>'You Can not set this value'], 400, lang('home.error_validation_form')); 
            }
            //----------------------------------------------------------------//

        }
        //---------------------------------------------------------------------//
        else
        {
            //not matched
            log_message('error', "SECURITY: $form_slug form not founded");
            return $this->failNotFound("$form_slug form not found");
        }

        $CountyModel = new CountyModel();

        try
        {
            $CountyModel->save($data);
        }
        catch (\Exception $e)
        {
            log_message('error', '[ERROR] {exception}', ['exception' => $e]);
            return $this->fail($e->getMessage(), 400, lang('home.error_validation_form'));
        }
        
        if(empty($countyid))
        {
            $countyid = $CountyModel->getInsertID();
        }

        return $this->respondCreated(['id' => $countyid, 'message' => lang('home.saved')]);
    }
 
