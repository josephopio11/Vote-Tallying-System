            $data['breadcrumb'][] = ['title' => lang('county._form_county'), 'class' => 'active'];
        }
        else
        {
            log_message('error', "SECURITY: $formSlug form not founded");
            return $this->failNotFound("$formSlug not founded");
        }

        if (!empty($id))
        {
            $CountyModel = new CountyModel();
            
            $data['formData'] += $CountyModel->find($id) ?? [];
        }

        $copy      = (int) $this->request->getGet('copy');

        if (!empty($copy))
        {
            $CountyModel = new CountyModel();
            
            $data['formData'] += $CountyModel->find($copy);
            
            if(isset($data['formData']['countyid']))
            {
                unset($data['formData']['countyid']);
            }
        }

        

        $this->_render('form/' . $formSlug, $data);
    }

