    /**
     * Read statistic Card Data
     * 
     * @param string $cardSlug
     * @return json
     */
    public function readStatistic($pageSlug, $cardSlug)
    {
        $cardData = [];

        //Page: county
        if($pageSlug === 'county' && $cardSlug === 'Counties')
        {
            $CountyModel = new CountyModel();
            $CountyModel->select('COUNT(countyid) as COUNT_countyid');

            //---------------------------------------------------------------------// 
            $CountyModel->where('county.status', '1');
            //---------------------------------------------------------------------//
            $CountyModel->where("deleted_at IS NULL");

            $cardData = $CountyModel->get()->getRowArray();
        }

        return $this->response->setJSON($cardData);
    }
 
